# Configuring each tests

class ConfigureUsernamePassword:

    def __int__(self):
        self.username
        self.password


class ConfigureProject:

    def __int__(self):
        self.project_name
        self.project_description
        self.student_file
        self.json_file


class ConfigureEvaluation:

    def __int__(self):
        self.project_name
        self.evaluation_name


class ConfigureRating:

    def __int__(self):
        self.metagroup_name
        self.group_name
        self.level
        self.checkbox1
        self.checkbox2
        self.checkbox3
        self.student_name_to_check
